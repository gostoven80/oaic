     <p  style='color:black;font-size:16px;margin:35px;'> 
     Aliment de base de l’humanité, les céréales ont représenté et représentent encore aujourd’hui
un produit de première nécessité.
L’OAIC est un établissement public à caractère industriel et commercial , est chargé de mettre en 
œuvre toutes les mesures pour appuyer, développer la production
des céréales et légumes secs et pour stabiliser les prix. L’OAIC rayonne sur le territoire national à 
travers</p>	
    <p  style='color:black;font-size:16px;margin:35px;'> 
En améliorant la conduite des itinéraires techniques, en sécurisant la
plus grande partie possible de notre production par l’irrigation et en
résorbant des superficies actuellement en jachère, les réserves de
productivité et de production seront comblées.
Les exemples de réseaux céréaliculteurs-transformateurs pour
l’amélioration de la production céréalière dans le cadre des CRIC (réseau
BENAMOR, réseau SIM, réseau SOPIE, réseau KERAGHEL, réseau
METIDJI), sont à méditer et à suivre par l’ensemble des opérateurs
désireux de développer la production céréalière en Algérie.
Notre objectif est d’atteindre des rendements et des productions à la
hauteur de nos potentialités
</p>
 <p  style='color:black;font-size:16px;margin:35px;'>
 La coopérative contribue à la satisfaction des besoins des consommateurs dans des conditions économique  et sociales les plus avantageuses pour les collectivité , chargée aussi de mettre en œuvre toutes les mesures pour appuyer , développer la production des céréales et dérivés et pour stabiliser le prix 
</p>

	
