      Bienvenu(e) sur le site de L'<b>OAIC</b><br/>( Office Algérien Interprofessionnel des Céréales )</p>
      <p  style='color:black;font-size:16px;margin:10px;'>
      créé par ordonnance du 12 Juillet 1962, a constitué l'opérateur national auquel a été confié une mission de service public en matière d'organisation du marché des céréales, d'approvisionnement, de régulation, de stabilisation des prix et d'appui à la production.
      
      
      <p  style='color:black;font-size:16px;margin:10px;'>
      Son activité a été ultérieurement à cette date, étendue aux autres produits tels que les avoines, les légumes secs et les semences fourragères. Des textes réglementaires périodiques définissant les prix et les modalités de commercialisation des céréales et légumes secs, lui ont attribué pour le compte de l'État, la gestion centralisée de la politique de stabilisation des prix.</p>
